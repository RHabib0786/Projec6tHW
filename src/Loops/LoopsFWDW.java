package Loops;

public class LoopsFWDW {


	public static void main(String[] args) {
		for(int p=1; p <=3000;p++) {
			System.out.println("Run until p is" + p);
		}
		for(int t=5; t>= -10;t--){
			System.out.println ("Walk until t" + t--);
		}
		//While Loop
	 
		int t= 100;
		while (t>0) {
			System.out.println(t);
		t--;}
		//Do While
		int R= 10;
		do {
			System.out.println("Task");
		}while(R>10);
		}
}
	
	
	